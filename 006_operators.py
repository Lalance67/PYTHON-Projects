# Arithmetic operators
x = 5
y = 2
print(x + y)   # 7
print(x / y)   # 2.5 (float)
print(x // y)  # 2   (floor division which divides the lowest value divisible to two)
print(x * y)   # 10
print(x ** y)  # 25  (exponentiation)
print(x % y)   # 1 (modulus)


# Comparison operators
a = 10
b = 5
print(a > b)    # True 
print(a == b)   # False
# same syntax in C


# Boolean / Logical Operators
x = 5
print(x > 0 and x < 10)  # True
print(not x > 0)          # False
# uses "and", "or", "not"


