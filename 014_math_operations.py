x -= 1   
x *= 3  
x /= 2 
x //= 2  
x %= 3   
x **= 2 

# math functions
print(abs(-7))       # absolute value
print(pow(2, 3))     # esponent
print(round(3.567))  # roundoff (to nearest int)
print(round(3.567, 2))  # roundoffs in the 2 dec places (3.57)
import math

print(math.sqrt(16))     # 4.0 (square root)
print(math.factorial(5)) # 120
print(math.gcd(12, 18))  # 6 (greatest common divisor)
print(math.pi)           # 3.141592...
print(math.e)            # 2.718281...
print(math.sin(math.pi/2))  # 1.0 (trigo)






